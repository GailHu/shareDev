# 变量和数据类型
# 学习Python中的变量和基本数据类型

# 定义变量
name = "Alice"
age = 25
height = 1.75
is_student = True

# 打印变量
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height}")
print(f"Is Student: {is_student}")

# 数据类型
print(type(name))  # str
print(type(age))   # int
print(type(height)) # float
print(type(is_student)) # bool